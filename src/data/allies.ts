// Helper functions
export const getAlliesByCategory = (category: Category): Ally[] => {
  return allies.filter(ally => ally.category === category);
};

export const getAlliesByState = (state: State): Ally[] => {
  return allies.filter(ally => ally.state === state);
};

export const getTucsonBusinesses = (): Ally[] => {
  return allies.filter(ally => ally.state === 'Arizona' && ally.city === 'Tucson' && ally.category === 'Business');
};

export const getTucsonOrganizations = (): Ally[] => {
  return allies.filter(ally => ally.state === 'Arizona' && ally.city === 'Tucson' && ally.category !== 'Business');
};

export const getNationalOrganizations = (): Ally[] => {
  return allies.filter(ally => ally.state === 'National');
};

export const searchAllies = (query: string): Ally[] => {
  const lowercaseQuery = query.toLowerCase();
  return allies.filter(ally => 
    ally.name.toLowerCase().includes(lowercaseQuery) ||
    ally.description.toLowerCase().includes(lowercaseQuery) ||
    ally.services.some(service => service.toLowerCase().includes(lowercaseQuery)) ||
    ally.city?.toLowerCase().includes(lowercaseQuery) ||
    ally.state.toLowerCase().includes(lowercaseQuery)
  );
};

